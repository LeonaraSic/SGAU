                              <hgroup>
                                   <h1>Entrevista com colaboradores da SEMAM</h1>
                               </hgroup>
                              
                               <p>
						     Em conversa informal com os funcionários da SEMAM, pudemos esclarecer diversas dúvidas que aparecerem no processo de aprendizado sobre arborização. Veja os destaques desse diálogo:
					     </p>
					
                              <p>
                                   <strong>Quais projetos de arborização estão sendo implantados atualmente nos bairros de Macapá?</strong>
                                   
                                   <br>R: No momento apenas a arborização das ruas principais do loteamento Morada das Palmeiras. Na sequência, os trabalhos serão iniciados no Bairro Renascer.
                              </p>
               
                              <p>
                                   <strong>Uma reportagem que apontava o índice de cobertura arbórea no município de 60%, porém mal distribuída. Quais bairros possuem menor cobertura arbórea?</strong>
                                   
                                   <br>R: São os bairros e conjuntos residenciais mais novos. Nos bairros mais antigos (como Infraero e Boné Azul) o que dificulta é o pequeno espaçamento da calçada, impossibilitando o plantio de árvores. Nestes casos, a campanha deve ser voltada ao plantio da espécie na área interna das residências.
                              </p>

                              <p>
                                   <strong>Caso o cidadão queira plantar uma árvore em sua calçada, quais procedimentos ele deve tomar? Quais os piores erros cometidos nesse processo?</strong>
                                   
                                   <br>R: Não precisa de autorização, apenas orientação. Pedimos ao cidadão que evite árvores frutíferas, devido aos prejuízos causados por queda dos frutos em carros e telhados. A espécie escolhida deve ser plantada bem afastada de caixas de inspeção. Não há problemas em plantar árvores de grande porte, desde que seja feita a poda preventiva, direcionando seu crescimento. Palmeiras também não são recomendadas, são muito altas e oferecerem pouca sombra. Não se podem plantar árvores nas esquinas, para que não atrapalhem a visibilidade dos veículos.
                              </p>

                              <p>
                                   <strong>Existe em Macapá uma listagem de árvores e espécies que podem ser plantadas nas calçadas, que não interfiram na rede de esgoto e elétrica?</strong>
                                   
                                   <br>R: Este é um trabalho em andamento pela Prefeitura, onde já possuímos diversas espécies catalogadas. Também está em vias de conclusão uma licitação para contratar um profissional que fará esse levantamento melhor detalhado, apontando as espécies mais adequadas para cada tipo de ambiente e que se adaptem ao clima da Região Norte, com árvores nativas do Estado, compondo assim o Manual Técnico de Arborização Urbana de Macapá.
                                   E onde as pessoas conseguem as mudas para plantio? A Prefeitura possui sementes em estoque ou o cidadão adquire no comércio da cidade?
                                   R: Aqui no Horto Municipal desenvolvemos mudas não só de árvores, mas de plantas ornamentais para paisagismo. O cidadão que tiver interesse em plantar árvores pode solicitar diretamente aqui na SEMAM. Nossa equipe também vai às residências e coleta mudas e frutos em decomposição, sementes caídas entre outros, para desenvolvimento e estudo de novas espécies no Horto.
                              </p>

                              <p>
                                   <strong>Quais as maiores dificuldades encontradas na manutenção das áreas verdes?</strong>
                              
                                   <br>R: Os projetos são implantados na época de chuvas, para que a irrigação natural e humidade favoreçam o desenvolvimento das espécies. Mas no verão as chuvas caem menos, o que leva muitas vezes à morte das mudas. Possuímos poucos profissionais e isso dificulta a irrigação das áreas. Temos reduzido o plantio de árvores frutíferas, no inverno as árvores encharcam e os galhos pesam, rompendo-se e causando danos. As mangueiras do bairro central têm suas copas podadas de forma que não atinjam a rede elétrica. A consequência são árvores descaracterizadas e com os galhos pendendo em direção à rua, o que pode mais tarde causar sérios acidentes. Podas irregulares e furtos de mudas recém-plantadas também ocorrem muito.
                              </p>
                              
                              <p>
                                   <strong>Como transcorreu o Congresso Brasileiro de Arborização Urbana realizado em nov./17? As ideias apresentadas podem ser aproveitadas pela prefeitura?</strong>
                                   
                                   <br>R: Foi positivo, com diversos estudos e projetos apresentados por acadêmicos e profissionais da área. Recebemos orientações da Sociedade Brasileira de Arborização Urbana no sentido de conter a distribuição de mudas de árvores frutíferas aos cidadãos, por não realizarem o plantio de forma correta. Exceção é quando se tem um pomar na residência. Neste caso, fornecemos as mudas e orientações necessárias, pois aquele espaço interno também é considerado área arborizada.
                              <p>
                              
                              <p>
                                   
                              
                                   <strong>Quais os principais motivos para os cidadãos virem até a SEMAM: reclamações ou esclarecimentos?</strong>
                                   
                                   <br>R: A maioria vem pedir a remoção de árvores, muitas delas em perfeito estado. Se não houver a possibilidade de mantê-las, o cidadão paga uma taxa à Prefeitura pelo sacrifício da árvore ou realiza o plantio de outra, em substituição da árvore derrubada. Essa medida é mais para coibir a remoção desenfreada de árvores que não apresentam risco algum à população. Quando a árvore já está velha ou infestada de pragas não se cobra taxa para remoção. E em menor escala, realizamos trabalhos de poda preventiva, quando a árvore apresenta riscos ao patrimônio do cidadão.
                              </p>
                        
